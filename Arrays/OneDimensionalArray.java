package javaOnline.package1.Arrays;

import java.util.Arrays;

public class OneDimensionalArray {
    public static void main(String[] args) {
        int[] arr=new int[4];

        arr[0]=10;
        arr[1]=20;
        arr[2]=30;
        arr[3]=40;

        System.out.println("Elements of index 0: "+arr[0]);
        System.out.println("Elements of index 1: "+arr[1]);
        System.out.println("Elements of index 2: "+arr[2]);
        System.out.println("Elements of index 3: "+arr[3]);
    }
}
