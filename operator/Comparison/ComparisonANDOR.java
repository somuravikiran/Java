package javaOnline.package1.operator.Comparison;

import java.util.Arrays;

public class ComparisonANDOR {
    public static void main(String[] args) {
        int a=1;
        int b=2;

        //using conditional And
        if (a==1&&b==2)
            System.out.println("a=1 and b=2");

        //using conditional OR
        if(a==1 || b==1)
            System.out.println("a=1 or b=1");
    }
}
