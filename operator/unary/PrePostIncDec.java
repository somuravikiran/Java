package javaOnline.package1.operator.unary;

import java.util.Arrays;

public class PrePostIncDec {
    public static void main(String[] args) {
        int i=1;
        i++;
        System.out.println(i);

        ++i;
        System.out.println(i);

        System.out.println(i++);

        System.out.println(++i);

        i--;
        System.out.println(i);

        --i;
        System.out.println(i);

        System.out.println(i--);

        System.out.println(--i);

    }
}
